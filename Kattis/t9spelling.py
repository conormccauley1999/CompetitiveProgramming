vs = [
    '2','22','222','3','33','333','4','44','444',
    '5','55','555','6','66','666','7','77','777','7777',
    '8','88','888','9','99','999','9999'
]

n = int(input())
for i in range(1, n + 1):
    o = f'Case #{i}: '
    s = input()
    l = ''
    for c in s:
        x = ''
        if c == ' ':
            x = '0'
            if x == l:
                x = ' ' + x
            l = '0'
        else:
            x = vs[ord(c) - 97]
            if x[0] == l:
                x = ' ' + x
            l = x[-1]
        o += x
    print(o)
